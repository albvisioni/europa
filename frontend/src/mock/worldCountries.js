// Group countries by region for easy access
export const countriesByRegion = worldCountries.reduce((acc, country) => {
  if (!acc[country.region]) {
    acc[country.region] = [];
  }
  acc[country.region].push(country);
  return acc;
}, {});

// Get country by code
export const getCountryByCode = (code) => {
  return worldCountries.find(country => country.code.toLowerCase() === code.toLowerCase());
};

// Get countries by region
export const getCountriesByRegion = (region) => {
  return countriesByRegion[region] || [];
};